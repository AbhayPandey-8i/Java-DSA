

static void main() {

    //create or declare
    int age;
    // This will show error --  System.out.println(age);

    //assign value
    age = 25;
    System.out.println(age);

    //define or initialize
    int totalMarks = 70;
    System.out.println(totalMarks);

    //Case Sensitive
    int weight = 60;
    int WEIGHT = 80;
    System.out.println("weight is:" + weight);
    System.out.println("WEIGHT is:" + WEIGHT);

    //starting letter
    int marks = 20;
    int MARKS = 30;
    int _marks = 40;
    int $marks = 50;

    //subsequent letter
    int height5 = 170;
    int abhay_pandey = 20;


    //No reserved keywords can be used for variable naming
    //int class = 35 -- This cannot be used

    //Variable name has no lenght limit
    int abhaypandeyfromshalimargarden = 100;

    //Variable name should be meaningful (Good practice)
    int a = 1;
    int b = 2;  // all these a b c are not meaningful
    int c = 3;

    //For constant value use UPPERCASE
    int DAYS_IN_YEAR = 365;


}